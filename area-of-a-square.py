'''Faça um Programa que calcule a área  de  um  quadrado,  em seguida  mostre  o  dobro  desta área para o usuário.'''
print('Calculo da area de um quadrado')
medidaUm = float(input('Digite a primeira medida do quadrado: '))
medidaDois = float(input('Digite a segundad medida do quadrado: '))

areaTotal = (medidaUm * medidaDois)*2

print('O dobro da área calculada é {}.'.format(areaTotal))
